			<p class="text_justify"> L’employeur peut planifier, après consultation des représentants du personnel, un certain
				nombre de formations dans l’année ou sur une période plus longue. Dans ce cadre, il est libre
				de décider :
				<li> d’envoyer ou non un salarié en formation</li>
				<li> d’interrompre la formation et de rappeler le salarié à son poste de travail Il lui incombe
				également de financer la formation et de maintenir la rémunération et la protection sociale du
			salarié en stage. </li></p>
