		<div class="tab-pane fade" id="profile" role="tabpanel" aria-labelledby="profile-tab">
			<p class="text_justify">Il lui incombe également de financer la formation et de maintenir la rémunération et la
				protection sociale du salarié en stage. Le congé individuel de formation (CIF) L’initiative de
				suivre une formation appartient ici au salarié qui remplit certaines conditions. Il choisit la
				formation qui lui convient. À son terme, le salarié retrouve son poste de travail ou
				l’équivalent. <br><br>
				Il existe d’autres congés avec des objectifs de formation plus spécifiques tels le
				congé de formation économique, sociale et syndicale, le congé de formation des membres du
				comité d’entreprise, le congé de bilan de compétences ou encore le congé pour validation des
			acquis de l’expérience.</p>
		</div>
		<div class="tab-pane fade" id="compte" role="tabpanel" aria-labelledby="compte-tab">
			<p class="text_justify">Le compte personnel de formation est un droit reconnu aux salariés disposant d’une
				ancienneté d’au moins un an dans l’entreprise :<br>
				ils en ont donc l’initiative, avec ou sans
				accord de l’employeur sur le choix de l’action de formation.<br><br> Les salariés en CDD peuvent
				également accéder à ce droit, dans des conditions spécifiques. Les apprentis, ainsi que les
				salariés en contrats de professionnalisation, ne sont pas concernés, une formation leur étant
			déjà dispensée dans le cadre de leur contrat.</p>
		</div>
		<div class="tab-pane fade" id="period" role="tabpanel" aria-labelledby="period-tab">
			<p class="text_justify">Les périodes de professionnalisation ont pour objet de favoriser, par des actions de formation,
				le maintien dans l’emploi de salariés en contrat à durée indéterminée (ou en CDI ou CDD
				dans le cadre d’un contrat unique d’insertion) rencontrant des difficultés particulières. Six
				catégories de bénéficiaires potentiels sont visées :
				<li> les salariés dont la qualification est insuffisante au regard de l’évolution des technologies
				et des organisations</li>
				<li> les salariés qui comptent vingt ans d’activité professionnelle ou âgés d’au moins 45 ans et
				disposent d’une ancienneté minimum d’un an de présence dans la dernière entreprise qui les
				emploie</li>

				<li> les salariés qui envisagent la création ou la reprise d’une entreprise</li>
				<li> les femmes qui reprennent une activité professionnelle après un congé de maternité ou aux
				personnes dans la même situation après un congé parental</li>
				<li> les bénéficiaires de l’obligation d’emploi prévue à l’article L. 5213-13 du Code du travail,
				notamment les travailleurs reconnus handicapés par la CDAPH</li>
				<li> les salariés bénéficiaires d’un contrat unique d’insertion (disposition en vigueur depuis le
				du 1er janvier 2010).</li>
				<li> La période de professionnalisation peut se dérouler pour tout ou partie en dehors du temps
			de travail.</li></p>
