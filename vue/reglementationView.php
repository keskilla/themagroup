<h1>LA REGLEMENTATION POUR LE CACES</h1>

<h3>Obtention du CACES</h3>
<p class="text-justify">
	Lors de l’examen CACES ®, ce sont les connaissances de la réglementation et la capacité à conduire en sécurité qui sont évaluées. Il est donc nécessaire de s’y préparer par une formation théorique et pratique. Théorie La formation théorique se déroule en groupe, dans une salle de cours et dure généralement une journée (7 heures). La formation théorique est différente selon la famille d’engins : engins de chantiers, chariots de manutention, nacelles, grues auxiliaires, etc.
</p>

<hr>

<h4>Pratique</h4>
<p class="text-justify">
	En groupe reduit (2 à 4 stagiaires par machines), se déroule sur une aire d’exercice spécifique dotée des équipements requis par les recommandations de la CNAMTS. La durée varie selon le niveau des stagiaires et le souhait du client. En général elle dure de 1 à 3 jours. La formation pratique est adaptée spécialement à la catégorie d’engins visée. Par exemple la formation pour la conduite d’une nacelle 3A est différente de la formation pour la conduite d’une nacelle 3B.
</p>
<hr ">

<h4>L’examen du CACES</h4>
<p class="text-justify">
	A l’issue de la formation, les stagiaires passent l’examen CACES ® afin de valider leur capacité à conduire en sécurité un engin spécifique. Ce document permettra ensuite à leur employeur de leur délivrer l’autorisation de conduite imposée par le Code du Travail (après vérification de l’aptitude médicale et formation aux risques inhérents aux lieux de travail). L’examen CACES ® comporte une partie théorique et une partie pratique. Il est réalisé par un testeur certifié, obligatoirement différent du formateur. Le testeur vérifiera tout le matériel avant le début du test. Un testeur ne peut réaliser plus de 6 tests pratiques par jour. 
</p>

